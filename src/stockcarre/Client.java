private void afficher_client() {
        model_client.setColumnCount(0);
        model_client.addColumn("ID");
        model_client.addColumn("Prénom/Nom");
        model_client.addColumn("Téléphone");
        model_client.addColumn("Adresse");
        model_client.addColumn("E-mail");
        
        btnClientModif.setEnabled(false);
        btnClientSuppr.setEnabled(false);
    
        try {
                model_client.setRowCount(0);
                stm = conn.obtenirconnexion().createStatement();
                ResultSet fr = stm.executeQuery("Select * from client order by id_client");
                    while (fr.next())   {
                        model_client.addRow(new Object[]{fr.getString("id_client"),fr.getString("nom_client"),
                            fr.getString("tel_client"), fr.getString("adresse"), fr.getString("mail")});
                                        }
            } catch (SQLException e) {System.err.println(e);}
        
        tbl_client.setModel(model_client);
        
        
        try {
                stm1 = conn.obtenirconnexion().createStatement();
                ResultSet fr2 = stm.executeQuery("Select count(*) from client");
                fr2.next();
                txtClientTotal.setText(fr2.getString(1));
               
                                        
            } catch (SQLException e) {System.err.println(e);}

}
    
    
    
    
