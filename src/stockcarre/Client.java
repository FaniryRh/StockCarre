    private void btnClientSupprActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClientSupprActionPerformed
        int i = tbl_client.getSelectedRow();
        String id = tbl_client.getValueAt(i, 0).toString();
        
            try {
                if (JOptionPane.showConfirmDialog(null, "voulez-vous vraiment supprimer: "+id+" ?" , "supprimer le client",
                    JOptionPane.YES_NO_OPTION) == JOptionPane.OK_OPTION) {
                if (id.length() != 0) {
                    stm.executeUpdate("Delete From client where id_client = '" + id+"'");
                    JOptionPane.showMessageDialog(null, "\""+id +"\" supprimé de la liste!");
                    //afficher_table();
                    
                } else {
                    JOptionPane.showMessageDialog(null, "Veuillez remplire le champ Numero");
                }
            }
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, "erreur de suppression \n" + e.getMessage());
        }
        
        
        
        
    }//GEN-LAST:event_btnClientSupprActionPerformed

