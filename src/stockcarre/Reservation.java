private void afficher_reserve() {
        model_reservation.setColumnCount(0);
        model_reservation.addColumn("ID");
        model_reservation.addColumn("Table");
        model_reservation.addColumn("Client");
        model_reservation.addColumn("Date");
        model_reservation.addColumn("Heure debut");
        model_reservation.addColumn("Heure fin");
        model_reservation.addColumn("Etat");
        

        btnReservationAnnul.setEnabled(false);
        btnRéservationTerminer.setEnabled(false);
    
        try {
                model_reservation.setRowCount(0);
                stm = conn.obtenirconnexion().createStatement();
                ResultSet fr = stm.executeQuery("Select * from reservation where etat != 'a' order by date, heur_debut desc");
                    while (fr.next())   {
                        model_reservation.addRow(new Object[]{fr.getString("id_reservation"),fr.getString("des_table"),
                            fr.getString("nom_client"), fr.getString("date"), fr.getTime("heur_debut"), fr.getTime("heur_fin")
                        , fr.getString("etat")});
                                        }
            } catch (SQLException e) {System.err.println(e);}
        
        tbl_reservation.setModel(model_reservation);
        
        
        try {
                stm1 = conn.obtenirconnexion().createStatement();
                ResultSet fr2 = stm.executeQuery("Select count(*) from reservation");
                fr2.next();
                jLabel2.setText(fr2.getString(1));
               
                                        
            } catch (SQLException e) {System.err.println(e);}

    
}



