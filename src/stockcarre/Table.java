private void afficher_table() {
        model_table.setColumnCount(0);
        model_table.addColumn("ID");
        model_table.addColumn("Designation");
        model_table.addColumn("Nombre de place");
        model_table.addColumn("Etat");
        
        btnTableModif.setEnabled(false);
        btnTableSuppr.setEnabled(false);
    
        try {
                model_table.setRowCount(0);
                stm = conn.obtenirconnexion().createStatement();
                ResultSet fr = stm.executeQuery("Select * from table1 order by id_table");
                    while (fr.next())   {
                        model_table.addRow(new Object[]{fr.getString("id_table"),fr.getString("designation_table"),
                            fr.getString("nbr_place"), fr.getString("etat")});
                                        }
            } catch (SQLException e) {System.err.println(e);}
        
        tbl_table.setModel(model_table);
        
        
        try {
                stm1 = conn.obtenirconnexion().createStatement();
                ResultSet fr2 = stm.executeQuery("Select count(*) from table1");
                fr2.next();
                txtTableTotal.setText(fr2.getString(1));
               
                                        
            } catch (SQLException e) {System.err.println(e);}
        
        try {
                stm1 = conn.obtenirconnexion().createStatement();
                ResultSet fr2 = stm.executeQuery("Select count(*) from table1 where etat='Active'");
                fr2.next();
                txtTableActive.setText(fr2.getString(1));
                
            } catch (SQLException e) {System.err.println(e);}
        
        
        try {
                stm1 = conn.obtenirconnexion().createStatement();
                ResultSet fr2 = stm.executeQuery("Select count(*) from table1 where etat='Inactive'");
                fr2.next();
                txtTableInactive.setText(fr2.getString(1));
                
            } catch (SQLException e) {System.err.println(e);}
        
 
}    
    
    
