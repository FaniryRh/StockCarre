    private void btnTableModifActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTableModifActionPerformed
        int i = tbl_table.getSelectedRow();
        String id_men = tbl_table.getValueAt(i, 0).toString();
        
        try{
                stm = conn.obtenirconnexion().createStatement();
                ResultSet fr = stm.executeQuery("Select * from reservation where id_table ='"+id_men+"'");
                if(fr.next()){
                    JOptionPane.showMessageDialog(null, "Impossible de modifier,\nCette table est encore sous réservation!");
                
                }else{
                    TableModif a = new TableModif();
                    a.setVisible(true);
                }
                        

            }catch (HeadlessException | SQLException ex) {
            }
 
    }//GEN-LAST:event_btnTableModifActionPerformed

