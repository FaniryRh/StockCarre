    private void btnTableSupprActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTableSupprActionPerformed
        int i = tbl_table.getSelectedRow();
        String id = tbl_table.getValueAt(i, 0).toString();
        if(id.length() == 0){JOptionPane.showMessageDialog(null, "veuillez séléctionner la table à supprimer");}else {
            try {
                if (JOptionPane.showConfirmDialog(null, "voulez-vous vraiment supprimer: "+id+" ?" , "supprimer une table",
                    JOptionPane.YES_NO_OPTION) == JOptionPane.OK_OPTION) {
                if (id.length() != 0) {

                    stm.executeUpdate("Delete From table1 where id_table = '" + id+"'");
                    JOptionPane.showMessageDialog(null, "\""+id +"\" supprimé de la liste!");
                    
                    
                    indic = 1;
                    
                } else {
                    JOptionPane.showMessageDialog(null, "Veuillez remplire le champ Numero");
                }
            }
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, "erreur de suppression \n" + e.getMessage());
        }
        }
    }//GEN-LAST:event_btnTableSupprActionPerformed

